def summation(a, b):
    '''
    Function for adding a and b
    '''
    result = a + b
    return result

def subtraction(a, b):
    '''
    Function for subtracting a from b
    '''
    result = a - b 
    return result

def multiplication(a, b):
    '''
    Function for multiplicating a with b
    '''
    result = a * b
    return result